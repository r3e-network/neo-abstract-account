/** Dynamically loads zh-CN translations on first use and merges them into MESSAGES. */
let zhCNLoading = null;
function loadZhCN() {
  if (MESSAGES['zh-CN']) return Promise.resolve();
  if (zhCNLoading) return zhCNLoading;
  zhCNLoading = import('./zh-CN.js')
    .then((mod) => {
      MESSAGES['zh-CN'] = mod.default;
    })
    .catch((e) => {
      if (import.meta.env.DEV) console.warn('[i18n] failed to load zh-CN translations:', e?.message);
    })
    .finally(() => {
      zhCNLoading = null;
    });
  return zhCNLoading;
}

let singleton = null;

function getStorage(storage) {
  if (storage) return storage;
  try {
    return globalThis.localStorage || null;
  } catch (err) {
    if (import.meta.env.DEV) console.warn('[i18n] localStorage access denied:', err?.message);
    return null;
  }
}

function normalizeLocale(locale) {
  return SUPPORTED_LOCALES.some((item) => item.code === locale) ? locale : DEFAULT_LOCALE;
}

function readStoredLocale(storage) {
  const backend = getStorage(storage);
  const stored = backend?.getItem(I18N_STORAGE_KEY) || DEFAULT_LOCALE;
  return normalizeLocale(stored);
}

function lookupMessage(locale, key) {
  return String(key || '')
    .split('.')
    .reduce((current, segment) => (current && typeof current === 'object' ? current[segment] : undefined), MESSAGES[locale]);
}

function syncDocumentLocale(locale) {
  try {
    if (globalThis.document?.documentElement) {
      globalThis.document.documentElement.lang = locale;
    }
  } catch (err) {
    if (import.meta.env.DEV) console.warn('[i18n] syncDocumentLocale failed:', err?.message);
  }
}

export function createI18nController({ storage = null, forceNew = false } = {}) {
  if (singleton && !forceNew) {
    return singleton;
  }

  const backend = getStorage(storage);
  const locale = ref(readStoredLocale(backend));
  syncDocumentLocale(locale.value);

  // Eagerly preload zh-CN if it is the stored locale so translations are ready quickly.
  if (locale.value === 'zh-CN' && !MESSAGES['zh-CN']) {
    loadZhCN();
  }

  const controller = {
    locale,
    locales: SUPPORTED_LOCALES,
    setLocale(nextLocale) {
      const normalized = normalizeLocale(nextLocale);
      if (normalized === 'zh-CN' && !MESSAGES['zh-CN']) {
        loadZhCN().then(() => {
          locale.value = normalized;
          backend?.setItem(I18N_STORAGE_KEY, normalized);
          syncDocumentLocale(normalized);
        });
        return;
      }
      locale.value = normalized;
      backend?.setItem(I18N_STORAGE_KEY, normalized);
      syncDocumentLocale(normalized);
    },
    t(key, fallback = '') {
      return lookupMessage(locale.value, key) ?? fallback ?? key;
    },
  };

  singleton = controller;
  return controller;
}

export function useI18n() {
  return createI18nController();
}
